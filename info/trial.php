        <h1> Trial Information</h1>

        <h2>Trial Conditions:</h2>
        Joining the League of Assassins core raid team works as follows:
        <br><br>
        On joining you will be promoted to the rank of " Trial Assassin
        ". This essentially means that you are a trial raid member
        and that if you pass your trial you will be accepted to the main team.
        <br><br>
        The trial period lasts two weeks with acceptance being based on the following:
        <br>

        <li>Attendance</li>
        <li>Performance</li>
        <li>Attitude</li>
        <li>Punctuality</li>
        <li>Suitability</li>

        <br>

        If you meet all of the criteria then following your two week trial you will be promoted to the core raid team
        with a
        rank of "Assassin".
        <br>

        <h2>Loot Rules as a Trial Assassin</h2>
        Loot is always prioritised to the core members in order to benefit the team and allow us to efficiently progress
        through
        content. Whilst this may seem unfair, trial members must understand that it would not benefit the team to give
        loot to
        those who are not necessarily going to be with us for very long.
        <br><br>
        With that being said, once you become a core raider, then loot will most likely be assigned to you quickly as
        you are
        likely to be missing several pieces of current content gear.
        <br><br>
        Once you are a core raider, there will be expectations of you that you can read <a href="http://www.leagueofassassins.co.uk/info/expectations.html">here</a>.

