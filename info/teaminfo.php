League of Assassins currently has one raid team which is raiding normal until complete and then aims to progress throughout heroic and eventually mythic.

We are currently a 15 man team and are aiming to expand to a 20 man team.

The three ranks that compose the raiders of the guild are as follows:


Core Assassins:

Core Assassins are the core raiders of the guild. They are expected to follow the expectations listed at expectations.html
and successfully research all fights and attend each raid with high levels of punctuality and the correct attitude.


Reserve Assassins:

Reserve Assassins are a group of 10-15 raiders who are periodically chosen to fill the remaining 5 spots of the core team depending on whether they can attend and how well they suit each individual fight.
The rank of reserve assassin allows those who are not necessarily able to attend every raid to still raid with us and progress.



Trial Assassins:

Upon joining League of Assassins with the intention to raid with us you will be subject to a trial, the details of which are here TRIAL.html.

